            <p>I am an Associate Professor at the <a href="http://www.emba.uvm.edu/math/">Department of Mathematics and Statistics</a>  of the University of Vermont. </p>
            <h2>Current Research</h2>
            <h3>Stability of Numerical Methods for Nonlinear Waves</h3>
            <h4>Abstract:</h4>
            <p>Recently I have shown that numerical instability of the well-known split-step (a.k.a. operator-splitting) method, when used to simulate a soliton of the nonlinear Schroedinger equation, has a number of unusual properties. Those properties cannot be described by the classical von Neumann stability analysis. Also, they are not seen in most other numerical schemes.</p>
            <p>I have proposed an extension of the von Neumann analysis that quantitatively explains these new properties.</p>
            <p>I am now considering generalizations of this work to other numerical schemes and other equations, where numerical instability of a spatially varying solution cannot be understood from the von Neumann analysis. Understanding details of such instabilities can help one to relax a tight bound on the step size and hence make the numerical method run faster. This research also creates a bridge between numerical analysis and a classical stability theory of linear partial differential equations in applied mathematics.</p>
            <p>Funding for a Ph.D. student is available on this project.  M.S. degree in Applied Mathematics or related field is required of all candidates. </p>

            <h2> Past Topics</h2>
