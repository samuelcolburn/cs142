                        <p>Small changes can have big impacts. In our usability testing, we asked users to replace a Toshiba laptop CPU. This website wants users to navigate to the guide page and select their device from a grid block and gradually sort though a series of submenus to find the appropriate guide. However, we found that users had difficulty on the original guide page finding where to start. We believe this is due to the misleading nature of the "PC" icon. The Toshiba we asked them to find a guide for is a laptop, and their "PC" picture is a desktop. This mislead a lot of users into thinking their computer was not covered by that subcategory. To solve this issue we changed the Icon to one that includes both a desktop and a laptop to more readily signify that it includes laptops. </p>
            <p>We also felt that the blue hover effect was distracting, and made it difficult to see the image of the item you selected. To make the images the focus of the hover, we changed the background effect to increase their visibility, rather than decrease it.</p>
